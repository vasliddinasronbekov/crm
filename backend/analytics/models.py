class AnalyticsSnapshot(models.Model):
    """
    Daily snapshot of platform metrics.{student_name},{date},{verification_code},{}
    Automatically generated by Celery or management command.
    """
    date = models.DateField(unique=True, db_index=True, help_text="Date of this snapshot")

    # User metrics
    total_users = models.PositiveIntegerField(default=0)
    total_students = models.PositiveIntegerField(default=0)
    total_teachers = models.PositiveIntegerField(default=0)
    active_students = models.PositiveIntegerField(default=0, help_text="Students who logged in this month")
    new_users_today = models.PositiveIntegerField(default=0)

    # Course & Group metrics
    total_groups = models.PositiveIntegerField(default=0)
    total_courses = models.PositiveIntegerField(default=0)
    active_groups = models.PositiveIntegerField(default=0, help_text="Groups with sessions this month")

    # Attendance metrics
    attendance_rate = models.DecimalField(max_digits=5, decimal_places=2, default=0, help_text="Average attendance percentage")
    total_classes_held = models.PositiveIntegerField(default=0)

    # Performance metrics
    average_exam_score = models.DecimalField(max_digits=5, decimal_places=2, default=0)
    passing_rate = models.DecimalField(max_digits=5, decimal_places=2, default=0, help_text="Percentage of students passing exams")

    # Financial metrics (in tiyin)
    daily_revenue = models.BigIntegerField(default=0)
    daily_expenses = models.BigIntegerField(default=0)
    daily_profit = models.BigIntegerField(default=0)
    pending_payments = models.BigIntegerField(default=0)

    # CRM metrics
    total_leads = models.PositiveIntegerField(default=0)
    active_leads = models.PositiveIntegerField(default=0)
    converted_leads_today = models.PositiveIntegerField(default=0)
    conversion_rate = models.DecimalField(max_digits=5, decimal_places=2, default=0)

    # Engagement metrics
    active_sessions = models.PositiveIntegerField(default=0, help_text="Active user sessions")
    avg_session_duration = models.PositiveIntegerField(default=0, help_text="Average session duration in seconds")
    total_logins = models.PositiveIntegerField(default=0)

    # System metrics
    total_tasks = models.PositiveIntegerField(default=0)
    pending_tasks = models.PositiveIntegerField(default=0)
    completed_tasks = models.PositiveIntegerField(default=0)

    # Metadata
    calculated_at = models.DateTimeField(auto_now=True)
    created_at = models.DateTimeField(auto_now_add=True)

    class Meta:
        ordering = ['-date']
        verbose_name = "Analytics Snapshot"
        verbose_name_plural = "Analytics Snapshots"
        indexes = [
            models.Index(fields=['-date']),
        ]

    def __str__(self):
        return f"Analytics Snapshot - {self.date}"

    @classmethod
    def generate_snapshot(cls, date=None):
        """Generate analytics snapshot for a specific date"""
        if date is None:
            date = timezone.now().date()

        from student_profile.models import Group, Course, Attendance, ExamScore, Payment, Expense
        from crm.models import Lead
        from task.models import Task

        # Calculate date range
        start_of_day = timezone.make_aware(timezone.datetime.combine(date, timezone.datetime.min.time()))
        end_of_day = start_of_day + timedelta(days=1)
        start_of_month = start_of_day.replace(day=1)

        # User metrics
        total_users = User.objects.filter(is_active=True).count()
        total_students = User.objects.filter(is_teacher=False, is_staff=False, is_active=True).count()
        total_teachers = User.objects.filter(is_teacher=True, is_active=True).count()
        new_users_today = User.objects.filter(date_joined__gte=start_of_day, date_joined__lt=end_of_day).count()

        # Active students (logged in this month)
        # This would require session tracking - placeholder for now
        active_students = total_students

        # Group & Course metrics
        total_groups = Group.objects.count()
        total_courses = Course.objects.count()
        active_groups = Group.objects.filter(
            start_day__lte=date,
            end_day__gte=date
        ).count()

        # Attendance metrics
        attendances_today = Attendance.objects.filter(date=date)
        total_classes = attendances_today.count()
        if total_classes > 0:
            present = attendances_today.filter(is_present=True).count()
            attendance_rate = (present / total_classes) * 100
        else:
            attendance_rate = 0

        # Performance metrics
        exam_scores_month = ExamScore.objects.filter(date__gte=start_of_month, date__lte=date)
        avg_score = exam_scores_month.aggregate(avg=Avg('score'))['avg'] or 0

        if exam_scores_month.exists():
            passing = exam_scores_month.filter(score__gte=60).count()
            passing_rate = (passing / exam_scores_month.count()) * 100
        else:
            passing_rate = 0

        # Financial metrics
        payments_today = Payment.objects.filter(
            date=date,
            status='paid'
        )
        daily_revenue = payments_today.aggregate(total=Sum('amount'))['total'] or 0

        expenses_today = Expense.objects.filter(date=date)
        daily_expenses = expenses_today.aggregate(total=Sum('amount'))['total'] or 0

        daily_profit = daily_revenue - daily_expenses

        pending = Payment.objects.filter(status='pending').aggregate(total=Sum('amount'))['total'] or 0

        # CRM metrics
        total_leads = Lead.objects.count()
        active_leads = Lead.objects.filter(status='in_progress').count()
        converted_today = Lead.objects.filter(
            status='converted',
            created_at__gte=start_of_day,
            created_at__lt=end_of_day
        ).count()

        leads_month = Lead.objects.filter(created_at__gte=start_of_month)
        if leads_month.exists():
            converted_month = leads_month.filter(status='converted').count()
            conversion_rate = (converted_month / leads_month.count()) * 100
        else:
            conversion_rate = 0

        # Task metrics
        try:
            total_tasks = Task.objects.count()
            pending_tasks = Task.objects.filter(is_done=False).count()
            completed_tasks = Task.objects.filter(is_done=True).count()
        except:
            total_tasks = pending_tasks = completed_tasks = 0

        # Create or update snapshot
        snapshot, created = cls.objects.update_or_create(
            date=date,
            defaults={
                'total_users': total_users,
                'total_students': total_students,
                'total_teachers': total_teachers,
                'active_students': active_students,
                'new_users_today': new_users_today,
                'total_groups': total_groups,
                'total_courses': total_courses,
                'active_groups': active_groups,
                'attendance_rate': attendance_rate,
                'total_classes_held': total_classes,
                'average_exam_score': avg_score,
                'passing_rate': passing_rate,
                'daily_revenue': daily_revenue,
                'daily_expenses': daily_expenses,
                'daily_profit': daily_profit,
                'pending_payments': pending,
                'total_leads': total_leads,
                'active_leads': active_leads,
                'converted_leads_today': converted_today,
                'conversion_rate': conversion_rate,
                'total_tasks': total_tasks,
                'pending_tasks': pending_tasks,
                'completed_tasks': completed_tasks,
            }
        )

        return snapshot


