class Report(models.Model):
    """
    Generated reports storage
    """
    REPORT_TYPES = [
        ('student-performance', 'Student Performance'),
        ('attendance-summary', 'Attendance Summary'),
        ('financial-report', 'Financial Report'),
        ('teacher-workload', 'Teacher Workload'),
        ('course-completion', 'Course Completion'),
        ('enrollment-trends', 'Enrollment Trends'),
        ('custom', 'Custom Report'),
    ]

    REPORT_STATUS = [
        ('generating', 'Generating'),
        ('completed', 'Completed'),
        ('failed', 'Failed'),
    ]

    report_id = models.CharField(max_length=100, unique=True, db_index=True)
    report_type = models.CharField(max_length=50, choices=REPORT_TYPES)
    title = models.CharField(max_length=255)
    description = models.TextField(blank=True)

    # Parameters
    period = models.CharField(max_length=20, default='month')
    start_date = models.DateField()
    end_date = models.DateField()

    # Data storage
    summary = models.JSONField(default=dict, help_text="Report summary statistics")
    data = models.JSONField(default=list, help_text="Report detailed data")
    charts = models.JSONField(default=list, help_text="Chart configurations")

    # Status
    status = models.CharField(max_length=20, choices=REPORT_STATUS, default='generating')
    error_message = models.TextField(blank=True)

    # Metadata
    generated_by = models.ForeignKey(User, on_delete=models.SET_NULL, null=True, related_name='generated_reports')
    generated_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    # File storage
    pdf_file = models.FileField(upload_to='reports/pdf/%Y/%m/', blank=True, null=True)
    csv_file = models.FileField(upload_to='reports/csv/%Y/%m/', blank=True, null=True)

    class Meta:
        ordering = ['-generated_at']
        verbose_name = "Report"
        verbose_name_plural = "Reports"
        indexes = [
            models.Index(fields=['-generated_at', 'report_type']),
            models.Index(fields=['generated_by', '-generated_at']),
        ]

    def __str__(self):
        return f"{self.title} - {self.generated_at.strftime('%Y-%m-%d')}"


